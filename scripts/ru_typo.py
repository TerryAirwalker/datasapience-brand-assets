#!/usr/bin/env python3
"""Русская микротипографика — неразрывные пробелы (висячие предлоги и связки).

Правило DS (типографика, полный набор): не оставлять в конце строки короткие
служебные слова и не разрывать неразрывные связки — приклеивать неразрывным
пробелом (\\u00A0). Применять ко ВСЕМ русским текстам в материалах перед рендером
(баннеры, слайды, документы, веб) и проверять в самопроверке (Шаг 4).

    from ru_typo import ru_nbsp
    headline = ru_nbsp("ведущий интегратор BI-систем в России")
    # -> "...BI-систем в России"  (предлог «в» не повиснет в конце строки)

В HTML \\u00A0 эквивалентен &nbsp;. Если нужны явные сущности — ru_nbsp(s, html=True).
"""
import re

NBSP = " "

# короткие служебные слова (1–3 буквы): предлоги, союзы, частицы
SHORT_WORDS = (
    "в во на за по из изо от ото до к ко с со о об обо у под подо над надо при без для про через "
    "и а но да или либо то ни не же бы ли б ж уж как что чем"
).split()

_SHORT_RE = re.compile(
    r"(^|[\s(«\"'>—–-])((?:%s))[ \t]+" % "|".join(sorted(map(re.escape, SHORT_WORDS), key=len, reverse=True)),
    re.IGNORECASE,
)

def _glue_short(m):
    return f"{m.group(1)}{m.group(2)}{NBSP}"

def ru_nbsp(text: str, html: bool = False) -> str:
    """Проставить неразрывные пробелы по правилам DS."""
    s = text
    # 1) короткие служебные слова → приклеить к следующему слову (повторяем для цепочек)
    for _ in range(3):
        s = _SHORT_RE.sub(_glue_short, s)
    # 2) тире не начинает строку: nbsp перед тире (слово — …)
    s = re.sub(r"[ \t]+([—–])", NBSP + r"\1", s)
    # 3) число + единица/слово не разрывать: 5 кг, 2026 год, 100 ₽, 65 +
    s = re.sub(r"(\d)[ \t]+([A-Za-zА-Яа-я%‰°₽$€+]+)", r"\1" + NBSP + r"\2", s)
    # 4) № / § / абз. + число
    s = re.sub(r"([№§])[ \t]+(\d)", r"\1" + NBSP + r"\2", s)
    # 5) инициалы и фамилия: И. И. Иванов
    s = re.sub(r"\b([А-Я])\.[ \t]*([А-Я])\.[ \t]*(?=[А-Я][а-я])", r"\1." + NBSP + r"\2." + NBSP, s)
    # 6) распространённые сокращения
    for a, b in [("т. е.", "т."+NBSP+"е."), ("т. д.", "т."+NBSP+"д."), ("т. п.", "т."+NBSP+"п."),
                 ("и т.", "и"+NBSP+"т.")]:
        s = s.replace(a, b)
    if html:
        s = s.replace(NBSP, "&nbsp;")
    return s

if __name__ == "__main__":
    for t in ["ведущий интегратор BI-систем в России",
              "Data Sapience — российский вендор",
              "20+ лет опыта и 65+ проектов",
              "команда И. И. Иванова работает с 5 кг данных"]:
        print(repr(ru_nbsp(t)))
